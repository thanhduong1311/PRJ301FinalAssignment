package Model;

/**
 *
 * @author Thanh Duong
 */
public class Organization {
    private int ID;
    private String name;
    private String logo;
    private String description;

    public Organization() {
    }

    public Organization(int ID, String name, String logo, String description) {
        this.ID = ID;
        this.name = name;
        this.logo = logo;
        this.description = description;
    }

    public int getID() {
        return ID;
    }

    public void setID(int ID) {
        this.ID = ID;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLogo() {
        return logo;
    }

    public void setLogo(String logo) {
        this.logo = logo;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }
    
    
    
}
